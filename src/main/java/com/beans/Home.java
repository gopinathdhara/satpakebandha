package com.beans;

public class Home {
	
	private long id;
	private String gender;
	private int agefrom;
	private int ageto;
	private int religiion;
	private int mother_tongue;
	private int caste;
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getAgefrom() {
		return agefrom;
	}
	public void setAgefrom(int agefrom) {
		this.agefrom = agefrom;
	}
	public int getAgeto() {
		return ageto;
	}
	public void setAgeto(int ageto) {
		this.ageto = ageto;
	}
	public int getReligiion() {
		return religiion;
	}
	public void setReligiion(int religiion) {
		this.religiion = religiion;
	}
	public int getMother_tongue() {
		return mother_tongue;
	}
	public void setMother_tongue(int mother_tongue) {
		this.mother_tongue = mother_tongue;
	}
	public int getCaste() {
		return caste;
	}
	public void setCaste(int caste) {
		this.caste = caste;
	}

	

}
